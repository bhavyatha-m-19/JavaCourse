package Arrays;

public class ArrayCreation {

    public static void main(String[] args){

        int nums[] = new int[4];

        nums[0] = 2;
        nums[1] = 4;
        nums[2] = 5;
        nums[3] = 10;

        for(int i = 0; i<4; i++){
            System.out.println(nums[i]);
        }
    }
    
}
